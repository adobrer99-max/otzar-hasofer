// Build-time generator: extract the 22 Hebrew letterform outlines from the
// self-hosted David Libre font into committed SVG path data, so the Herald
// renderer can draw the letters as gold-leaf-filled <path>s instead of live
// <text> — removing the web-font dependency from SVG export and letting the
// glyphs carry the illumination.
//
// David Libre is licensed OFL-1.1 (see public/fonts/OFL.txt), which permits
// extracting and embedding glyph outlines. Run: `npm run build:letterpaths`.
//
// Output: src/herald/render/letterPaths.generated.ts. Paths are in the font's
// own em units (y-up), together with unitsPerEm, each glyph's ink bbox, and
// advance width — the renderer scales/flips them at draw time.

import * as fontkit from "fontkit";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const FONT = join(root, "public/fonts/david-libre-400-hebrew.woff2");
const OUT = join(root, "src/herald/render/letterPaths.generated.ts");

// id -> glyph char, read straight from the letter data so the two never drift.
const lettersSrc = readFileSync(join(root, "src/data/letters.ts"), "utf8");
const ids = [...lettersSrc.matchAll(/id:\s*"([a-z]+)"/g)].map((m) => m[1]);
const glyphs = [...lettersSrc.matchAll(/glyph:\s*"([^"]+)"/g)].map((m) => m[1]);
if (ids.length !== 22 || glyphs.length !== 22) {
  throw new Error(`Expected 22 letters, found ${ids.length} ids / ${glyphs.length} glyphs`);
}

const font = fontkit.openSync(FONT);
const entries = ids.map((id, i) => {
  const cp = glyphs[i].codePointAt(0);
  const glyph = font.glyphForCodePoint(cp);
  const d = glyph.path.toSVG();
  const { minX, minY, maxX, maxY } = glyph.bbox;
  return { id, d, bbox: { minX, minY, maxX, maxY }, advance: glyph.advanceWidth };
});

const body = entries
  .map(
    (e) =>
      `  ${e.id}: { d: ${JSON.stringify(e.d)}, bbox: { minX: ${e.bbox.minX}, minY: ${e.bbox.minY}, maxX: ${e.bbox.maxX}, maxY: ${e.bbox.maxY} }, advance: ${e.advance} },`,
  )
  .join("\n");

const out = `// GENERATED by scripts/build-letterpaths.mjs — do not edit by hand.
// The 22 Hebrew letterform outlines from David Libre (OFL-1.1; see
// public/fonts/OFL.txt), in font em units (y-up). The Herald renderer scales
// and flips these to draw illuminated, font-independent charge glyphs.

export interface LetterPath {
  /** SVG path 'd', in font units, y-up (baseline at y=0). */
  d: string;
  /** Ink bounding box in font units. */
  bbox: { minX: number; minY: number; maxX: number; maxY: number };
  /** Advance width in font units. */
  advance: number;
}

export const UNITS_PER_EM = ${font.unitsPerEm};

export const letterPaths: Record<string, LetterPath> = {
${body}
};
`;

writeFileSync(OUT, out);
console.log(`Wrote ${OUT} — ${entries.length} glyphs, unitsPerEm=${font.unitsPerEm}`);
